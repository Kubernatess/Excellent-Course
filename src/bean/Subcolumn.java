package bean;

public class Subcolumn {
	private String teacherIdentity;
	private String courseName;
	private int tabIndex;
	private int columnIndex;
	private int subcolumnIndex;
	private String subcolumnName;
	private String content;
		
	public String getTeacherIdentity() {
		return teacherIdentity;
	}
	public void setTeacherIdentity(String teacherIdentity) {
		this.teacherIdentity = teacherIdentity;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public int getTabIndex() {
		return tabIndex;
	}
	public void setTabIndex(int tabIndex) {
		this.tabIndex = tabIndex;
	}
	public int getColumnIndex() {
		return columnIndex;
	}
	public void setColumnIndex(int columnIndex) {
		this.columnIndex = columnIndex;
	}
	public int getSubcolumnIndex() {
		return subcolumnIndex;
	}
	public void setSubcolumnIndex(int subcolumnIndex) {
		this.subcolumnIndex = subcolumnIndex;
	}
	public String getSubcolumnName() {
		return subcolumnName;
	}
	public void setSubcolumnName(String subcolumnName) {
		this.subcolumnName = subcolumnName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
